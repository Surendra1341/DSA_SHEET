package com.first_java02;

import java.util.Scanner;

public class OddEven {
    public static void main(String[] args) {
        Scanner input= new Scanner(System.in);
        int a= input.nextInt();
        if(a%2==0)
            System.out.println("even");
        else
            System.out.println("odd");
    }

}

